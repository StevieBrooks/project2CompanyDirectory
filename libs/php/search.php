<?php

ini_set('display_errors', 'On');
	error_reporting(E_ALL);

	$executionStartTime = microtime(true);

	include("config.php");

	header('Content-Type: application/json; charset=UTF-8');

	$conn = new mysqli($cd_host, $cd_user, $cd_password, $cd_dbname, $cd_port, $cd_socket);

	if (mysqli_connect_errno()) {
		
		$output['status']['code'] = "300";
		$output['status']['name'] = "failure";
		$output['status']['description'] = "database unavailable";
		$output['status']['returnedIn'] = (microtime(true) - $executionStartTime) / 1000 . " ms";
		$output['data'] = [];

		mysqli_close($conn);

		echo json_encode($output);

		exit;

	}	


	$query = 'SELECT personnel.id, personnel.firstName, personnel.lastName, personnel.email, personnel.jobTitle, department.name AS department
	FROM personnel
	INNER JOIN department ON personnel.departmentID = department.id
	WHERE personnel.firstName LIKE ? OR personnel.lastName LIKE ? OR personnel.email LIKE ? ORDER BY personnel.lastName';

	error_log(print_r($_REQUEST, true));


    $param = "%" . $_GET['empQuery'] . "%";

	$stmt = $conn->prepare($query);
	$stmt->bind_param("sss", $param, $param, $param);
	$stmt->execute();
	$result = $stmt->get_result();
	
	if (!$result) {

		$output['status']['code'] = "400";
		$output['status']['name'] = "executed";
		$output['status']['description'] = "query failed";	
		$output['data'] = [];

		mysqli_close($conn);

		echo json_encode($output); 

		exit;

	}
   
   	$data = [];

	while ($row = mysqli_fetch_assoc($result)) {

		array_push($data, $row);

	}

	$output['status']['code'] = "200";
	$output['status']['name'] = "ok";
	$output['status']['description'] = "success";
	$output['status']['returnedIn'] = (microtime(true) - $executionStartTime) / 1000 . " ms";
	$output['data'] = $data;
	
	mysqli_close($conn);

	echo json_encode($output); 

?>